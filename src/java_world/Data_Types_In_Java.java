package java_world;

public class Data_Types_In_Java {

	public static void main(String[] args) {
		// Every variable is associate with a data type 
		
		//For Normal Value 
		int a = 50;
		System.out.println(a);
		
		// For decimal Value 
		float b = 12.5f;
		System.out.println(b);
		
		// Use for true / false value 
		Boolean c = true;
		System.out.println(c);
		
		// use for character value 
		char myChar = 'E';
		System.out.println(myChar);                      // should come E
		
		// Use for write word / sentence 
	    String g = "This is my string value";   
	    System.out.println("g"+g);                           // should come g
	

	}

}
